// MARK: - UIColor brightness helper

extension UIColor {
    /// Returns a new color with brightness adjusted by the given amount (0.0-1.0 scale).
    func adjustedBrightness(by amount: CGFloat) -> UIColor {
        var hue: CGFloat = 0
        var saturation: CGFloat = 0
        var brightness: CGFloat = 0
        var alpha: CGFloat = 0

        guard getHue(&hue, saturation: &saturation, brightness: &brightness, alpha: &alpha) else {
            return self
        }

        return UIColor(
            hue: hue,
            saturation: saturation,
            brightness: min(brightness + amount, 1.0),
            alpha: alpha
        )
    }
}
